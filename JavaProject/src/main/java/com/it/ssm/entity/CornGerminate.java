package com.it.ssm.entity;

public class CornGerminate {
	private int imageId;
	private String imageName;
	private String algorithm;
	private Object sumCount;
	private Object germinationCount;
	private Object nogerminationCount;
	private Object germinationRate;
	public int getImageId() {
		return imageId;
	}
	public void setImageId(int imageId) {
		this.imageId = imageId;
	}
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public String getAlgorithm() {
		return algorithm;
	}
	public void setAlgorithm(String algorithm) {
		this.algorithm = algorithm;
	}
	public Object getSumCount() {
		return sumCount;
	}
	public void setSumCount(Object sumCount) {
		this.sumCount = sumCount;
	}
	public Object getGerminationCount() {
		return germinationCount;
	}
	public void setGerminationCount(Object germinationCount) {
		this.germinationCount = germinationCount;
	}
	public Object getNogerminationCount() {
		return nogerminationCount;
	}
	public void setNogerminationCount(Object nogerminationCount) {
		this.nogerminationCount = nogerminationCount;
	}
	public Object getGerminationRate() {
		return germinationRate;
	}
	public void setGerminationRate(Object germinationRate) {
		this.germinationRate = germinationRate;
	}
	

}
