package com.it.ssm.entity;

public class CornTotal {
	private Object name;
	private Object lengthMeans;
	private Object widthMeans;
	private Object number;
	public Object getName() {
		return name;
	}
	public void setName(Object name) {
		this.name = name;
	}
	public Object getLengthMeans() {
		return lengthMeans;
	}
	public void setLengthMeans(Object lengthMeans) {
		this.lengthMeans = lengthMeans;
	}
	public Object getWidthMeans() {
		return widthMeans;
	}
	public void setWidthMeans(Object widthMeans) {
		this.widthMeans = widthMeans;
	}
	public Object getNumber() {
		return number;
	}
	public void setNumber(Object number) {
		this.number = number;
	}
	

}
